const character = "#"; // controls char used in pyramid
const count = 8; // controls number of rows in pyramid
const rows = [];
let inverted = true; // controls inversion of pyramid

// controls spacing for pyramid
function padRow(rowNumber, rowCount) {
  return " ".repeat(rowCount - rowNumber) + character.repeat(2 * rowNumber - 1) + " ".repeat(rowCount - rowNumber);
}

// controls creation of pyramid
for (let i = 1; i <= count; i++) {
  if (inverted) {
    rows.unshift(padRow(i, count));
  } else {
    rows.push(padRow(i, count));
  }
}

let result = ""

for (const row of rows) {
  result = result + row + "\n";
}

console.log(result);
